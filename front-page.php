		<p>
			The project "Nice to stamp you" began with the words of a person who visited my studio to order an original hand-carved stamp. She said: "Each person possesses a unique quality or 'essence'. If people can express their essences well, they can function better (in the society) and the whole environment would also benefit from it." As her words started to sink in, and idea rose to my mind: Maybe my stamps can also help people better express their essences...<br>
