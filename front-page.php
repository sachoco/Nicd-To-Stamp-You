		<h2>About Masa</h2>
		<div class="about__text">
			<p>
				Masaaki greatly values the unique inner nature of every person and is always conscious of his connections to those qualities. In his lifetime’s project “Nice to stamp you”, Masaaki uses himself as a filter to sieve the essences of people he interviews and expresses those in the form of stamps. The interactive interview sessions are often experienced as a sort of therapy, but the scope of the project does not stop there. Masaaki believes that it is his mission to translate what he felt in the interview into a physical matter -in this case, a stamp- and present it to the person who he interviewed. By doing so, Masaaki hopes that the person will never lose sight of who he or she is, and that by stamping the image, the person can better present his or her unique quality to others.
			</p>
			<p>
				MASAAKI OYAMADA<br>
				B. 1980, Yamanashi, Japan. Masaaki grew up surrounded by nature at the foot of Mount Fuji and studied design in Tokyo. In 2005, he moved to Europe and started working as a graphic designer. His clients include, among others, Royal Philips.
